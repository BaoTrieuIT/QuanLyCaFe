    boolean Check() {
        if (!checkMaMon() || !checkTenMon() || !checkSize() || !CheckGiaNhap() || !CheckGiaBan() || !CheckSoLuong() || !CheckNgayHetHan()) {
            return false;
        }
        return true;
    }

    boolean CheckUpdate() {
        if (!checkMaMonUpdate() || !checkTenMon() || !checkSize() || !CheckGiaNhap() || !CheckGiaBan() || !CheckSoLuong() || !CheckNgayHetHan()) {
            return false;
        }
        return true;
    }
